<h3>Tentang Kami</h3>
<p>Berita Sepakbola adalah Portal berita yang menyajikan berita-berita seputar sepakbola dari dalam negeri maupun dari luar negeri, 
 Konten yang dipublikasikan di Berita Sepakbola diperoleh dari media berita lainnya yang terpercaya.
</p>
 <p>
Berita Sepakbola bertujuan untuk memberikan informasi di dunia sepakbola kepada masyarakat lewat konten yang terkini, dan informatif.</p>  